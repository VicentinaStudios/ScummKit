import Foundation

/// A struct representing global resource information for Scumm games.
struct GlobalResource {
    
    /// The number of rooms in the game.
    static var numberOfRooms = 0
    
    /// The number of scripts in the game.
    static var numberOfScripts = 0
    
    /// The number of sounds in the game.
    static var numberOfSounds = 0
    
    /// The number of costumes in the game.
    static var numberOfCostumes = 0
    
    /// The number of global objects in the game.
    static var numberOfGlobalObject = 0
    
    /// The number of variables in the game.
    static var numberOfVariables = 0
    
    /// The number of bit variables in the game.
    static var numberOfBitVariable = 0
    
    /// The number of local objects in the game.
    static var numberOfLocalObjects = 0
    
    /// The number of character sets in the game.
    static var numberOfCharacterSets = 0
    
    /// The number of inventory objects in the game.
    static var numberOfInventoryObjects = 0
    
    /// The number of arrays in the game.
    static var numberOfArrays = 0
    
    /// The number of verbs in the game.
    static var numberOfVerbs = 0
    
    /// The number of new names in the game.
    static var numberOfNewNames = 0
    
    /// The number of global scripts in the game.
    static var numberOfGlobalScripts = 0
    
    /// The size of the shadow palette in the game.
    static var shadowPaletteSize = 0
}
